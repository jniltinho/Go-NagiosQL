// --- Formatting helpers ---

// cmdField formats a key=value pair matching PHP NagiosQL commands.cfg output:
// TAB indent, key padded to 31 chars, TAB separator, then value.
func cmdField(key, value string) string {
	pad := max(1, 31-len(key))
	return "\t" + key + strings.Repeat(" ", pad) + "\t" + value + "\n"
}

// bangDecode converts NagiosQL's internal bang-escape back to the '!'
// separator that Nagios uses for command arguments.
func bangDecode(s string) string {
	s = strings.ReplaceAll(s, `\::bang::`, "!")
	s = strings.ReplaceAll(s, "::bang::", "!")
	return s
}

// sanitizeName removes characters unsafe for filenames.
func sanitizeName(name string) string {
	r := strings.NewReplacer("/", "_", "\\", "_", ":", "_", " ", "_")
	return r.Replace(name)
}

