// Generator writes Nagios configuration files from the database.
type Generator struct {
	db         *gorm.DB
	hostDir    string
	serviceDir string
	backupDir  string
	appVersion string
}

// New creates a Generator using explicit directory paths.
// appVersion is the Go binary version injected at build time (e.g. "v1.2.3").
func New(db *gorm.DB, hostDir, serviceDir, backupDir, appVersion string) *Generator {
	if appVersion == "" {
		appVersion = buildinfo.Version
	}
	return &Generator{
		db:         db,
		hostDir:    hostDir,
		serviceDir: serviceDir,
		backupDir:  backupDir,
		appVersion: appVersion,
	}
}

// fileHeader returns the standard PHP NagiosQL file header for the given description.
func (g *Generator) fileHeader(description string) string {
	sep := "###############################################################################"
	return sep + "\n" +
		"#\n" +
		"# " + description + "\n" +
		"#\n" +
		"# Created by: Go-NagiosQL Version " + g.appVersion + "\n" +
		"# Date:\t      " + time.Now().Format("2006-01-02 15:04:05") + "\n" +
		"# Version:    Nagios 4.x config file\n" +
		"#\n" +
		"# --- DO NOT EDIT THIS FILE BY HAND --- \n" +
		"# Nagios QL will overwite all manual settings during the next update\n" +
		"#\n" +
		sep + "\n" +
		"\n"
}

// fileFooter returns the standard PHP NagiosQL file footer for the given description.
func (g *Generator) fileFooter(description string) string {
	sep := "###############################################################################"
	return sep + "\n" +
		"#\n" +
		"# " + description + "\n" +
		"#\n" +
		"# END OF FILE\n" +
		"#\n" +
		sep + "\n"
}

// wrapFile prepends the header and appends the footer to the given content.
func (g *Generator) wrapFile(description string, content []byte) []byte {
	var b bytes.Buffer
	b.WriteString(g.fileHeader(description))
	b.Write(content)
	b.WriteString(g.fileFooter(description))
	return b.Bytes()
}

// WriteHost renders and writes a single host .cfg file.
func (g *Generator) WriteHost(hostID uint) error {
	var host models.Host
	if err := g.db.First(&host, hostID).Error; err != nil {
		return fmt.Errorf("host %d not found: %w", hostID, err)
	}
	content, err := g.renderHost(&host)
	if err != nil {
		return err
	}
	filename := sanitizeName(host.HostName) + ".cfg"
	return g.writeWithBackup(filepath.Join(g.hostDir, filename),
		g.wrapFile("Host configuration file", content))
}

// WriteServiceGroup writes one .cfg file containing all services with the given config_name.
func (g *Generator) WriteServiceGroup(configName string) error {
	var svcs []models.Service
	if err := g.db.Where("config_name = ? AND active = '1'", configName).
		Order("service_description").Find(&svcs).Error; err != nil {
		return fmt.Errorf("loading services %q: %w", configName, err)
	}
	if len(svcs) == 0 {
		return nil
	}
	var buf bytes.Buffer
	for _, svc := range svcs {
		chunk, err := g.renderService(&svc)
		if err != nil {
			return err
		}
		buf.Write(chunk)
		buf.WriteByte('\n')
	}
	filename := sanitizeName(configName) + ".cfg"
	return g.writeWithBackup(filepath.Join(g.serviceDir, filename),
		g.wrapFile("Service configuration file", buf.Bytes()))
}

// WriteAllHosts writes a .cfg file for every active host.
func (g *Generator) WriteAllHosts() (int, error) {
	var hosts []models.Host
	if err := g.db.Where("active = '1'").Find(&hosts).Error; err != nil {
		return 0, err
	}
	var written int
	for _, h := range hosts {
		if err := g.WriteHost(h.ID); err != nil {
			return written, err
		}
		written++
	}
	return written, nil
}

// WriteAllServices writes one .cfg file per distinct config_name.
func (g *Generator) WriteAllServices() (int, error) {
	var names []string
	g.db.Model(&models.Service{}).Distinct("config_name").Where("active = '1'").Pluck("config_name", &names)
	var written int
	for _, name := range names {
		if err := g.WriteServiceGroup(name); err != nil {
			return written, err
		}
		written++
	}
	return written, nil
}

// WriteAll writes all object types and returns total files written.
func (g *Generator) WriteAll() (int, error) {
	base := filepath.Dir(strings.TrimRight(g.hostDir, "/"))

	singles := []struct {
		name string
		fn   func(string) error
	}{
		{"commands.cfg", g.WriteCommands},
		{"contactgroups.cfg", g.WriteContactGroups},
		{"contacts.cfg", g.WriteContacts},
		{"contacttemplates.cfg", g.WriteContactTemplates},
		{"hostgroups.cfg", g.WriteHostGroups},
		{"hosttemplates.cfg", g.WriteHostTemplates},
		{"hostdependencies.cfg", g.WriteHostDependencies},
		{"hostescalations.cfg", g.WriteHostEscalations},
		{"hostextinfo.cfg", g.WriteHostExtInfo},
		{"servicetemplates.cfg", g.WriteServiceTemplates},
		{"servicegroups.cfg", g.WriteServiceGroups},
		{"servicedependencies.cfg", g.WriteServiceDependencies},
		{"serviceescalations.cfg", g.WriteServiceEscalations},
		{"serviceextinfo.cfg", g.WriteServiceExtInfo},
		{"timeperiods.cfg", g.WriteTimeperiods},
	}

	total := 0
	h, err := g.WriteAllHosts()
	if err != nil {
		return total, err
	}
	total += h

	s, err := g.WriteAllServices()
	if err != nil {
		return total, err
	}
	total += s

	for _, item := range singles {
		if err := item.fn(filepath.Join(base, item.name)); err != nil {
			return total, fmt.Errorf("%s: %w", item.name, err)
		}
		total++
	}
	return total, nil
}

// WriteCommands writes all active commands to a single commands.cfg file,
// matching the PHP NagiosQL output format (tab-indented, key padded to 31 chars).
func (g *Generator) WriteCommands(outputFile string) error {
	var cmds []models.Command
	if err := g.db.Where("active = '1'").Order("command_name").Find(&cmds).Error; err != nil {
		return fmt.Errorf("loading commands: %w", err)
	}
	var buf bytes.Buffer
	for _, cmd := range cmds {
		buf.WriteString("define command {\n")
		buf.WriteString(cmdField("command_name", cmd.CommandName))
		buf.WriteString(cmdField("command_line", bangDecode(cmd.CommandLine)))
		// register=1 is always written for commands (PHP NagiosQL behavior).
		buf.WriteString(cmdField("register", cmd.Register))
		buf.WriteString("}\t\n\n")
	}
	return g.writeWithBackup(outputFile,
		g.wrapFile("Check command configuration file", buf.Bytes()))
}

