// resolveServiceCheckCmd resolves a check_command value stored as "<id>::bang::<args>"
// or "<id>!<args>" into "<command_name>!<args>".
func (g *Generator) resolveServiceCheckCmd(raw string) string {
	if raw == "" || raw == "0" {
		return ""
	}
	decoded := bangDecode(raw)
	parts := strings.SplitN(decoded, "!", 2)
	id, err := strconv.Atoi(strings.TrimSpace(parts[0]))
	if err != nil || id == 0 {
		return decoded
	}
	var name string
	g.db.Raw(`SELECT command_name FROM tbl_command WHERE id = ? LIMIT 1`, id).Scan(&name)
	if name == "" {
		return decoded
	}
	if len(parts) == 2 && parts[1] != "" {
		return name + "!" + parts[1]
	}
	return name
}

